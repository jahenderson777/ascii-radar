(defn add-unknowns [sample w h]
  (let [lines (str/split-lines sample)
        middle (map (fn [l]
                      (let [u (apply str (repeat w "U"))]
                        (str u l u)))
                    lines)
        start-end (repeat h (apply str (repeat (+ (count (first lines)) (* 2 w)) 
                                               "U")))]
    (vec (concat start-end middle start-end))))

