(defn score [a b]
  (let [paired (map vector a b)
        s (reduce (fn [score [ax bx]]
                    (if (or (= ax \U)
                            (= bx \U))
                      score ;; we have an Unknown location, e.g. beyond the edge
                      (if (= ax bx)
                        (+ score 1) ;; we have a match
                        score ;; we have no match
                        )))
                  0 paired)]
    (float (/ s (count a)))))

